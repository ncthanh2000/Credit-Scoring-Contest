from keras_preprocessing.sequence import pad_sequences

maxlen = 30


def predict_email(model, tokenizer, email):
    if not isinstance(email, list):
        email = [email]

    sequences_val = tokenizer.texts_to_sequences(email)
    padded_sequences_val = pad_sequences(sequences_val, maxlen=maxlen)
    results = model.predict(to_categorical(padded_sequences_val, tokenizer.num_words))
    return results


